public class City {

	String name;
	boolean isCapital;
	boolean isRegionCenter;
	double square;

	public City() {

	}

	public City(String name, boolean isCapital, boolean isRegionCenter, double square) {
this.name = name;
this.isCapital = isCapital;
this.isRegionCenter = isRegionCenter;
this.square = square;
	}
}
